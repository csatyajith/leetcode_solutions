from typing import List
from collections import Counter

class Solution:
    def numTriplets(self, nums1: List[int], nums2: List[int]) -> int:
        n_triplets = 0
        count1 = Counter()
        count2 = Counter()
        for i, n1 in enumerate(nums1):
            for j, n2 in enumerate(nums1[i+1:]):
                count1[n1*n2] += 1
        for i, n1 in enumerate(nums2):
            for j, n2 in enumerate(nums2[i+1:]):
                count2[n1*n2] += 1

        for i, n1 in enumerate(nums1):
            n_triplets += count2[n1*n1]
        for i, n2 in enumerate(nums2):
            n_triplets += count1[n2*n2]
        return n_triplets


