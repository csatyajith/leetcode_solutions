from typing import List


class Solution:
    def largestOverlap(self, A: List[List[int]], B: List[List[int]]) -> int:
        max_overlap = 0
        for row in range(len(A)):
            for col in range(len(A)):
                overlap = 0
                for r_row, m_row in enumerate(range(row, len(A))):
                    for r_col, m_col in enumerate(range(col, len(A))):
                        if B[m_row][m_col] == 1 and B[m_row][m_col] == A[r_row][r_col]:
                            overlap += 1

                if overlap > max_overlap:
                    max_overlap = overlap

                overlap = 0
                for r_row, m_row in enumerate(range(row, len(A))):
                    for r_col, m_col in enumerate(range(col, len(A))):
                        if A[m_row][m_col] == 1 and A[m_row][m_col] == B[r_row][r_col]:
                            overlap += 1

                if overlap > max_overlap:
                    max_overlap = overlap
        return max_overlap


